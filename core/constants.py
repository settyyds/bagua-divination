# 9. Month Gan — 五虎遁年起月法 (BUG B-06 FIXED)
#    Lookup: (year_gan, month_dizhi) → month_gan
# ─────────────────────────────────────────────────────────────

# 五虎遁年起月口诀:
# 甲己年 → 寅月起丙寅
# 乙庚年 → 寅月起戊寅
# 丙辛年 → 寅月起庚寅
# 丁壬年 → 寅月起壬寅
# 戊癸年 → 寅月起甲寅
_MONTH_GAN_START: Dict[str, str] = {
    "甲": "丙", "己": "丙",
    "乙": "戊", "庚": "戊",
    "丙": "庚", "辛": "庚",
    "丁": "壬", "壬": "壬",
    "戊": "甲", "癸": "甲",
}

def get_month_gan(year_gan: str, month_dizhi: str) -> str:
    """
    Return the TianGan for a given month using 五虎遁年起月法.
    month_dizhi must be one of the 12 earthly branches in month order
    (寅=1st month, 卯=2nd, …, 丑=12th).
    """
    start_gan = _MONTH_GAN_START[year_gan]
    start_idx = TIANGAN_INDEX[start_gan]
    # Month order starting at 寅
    month_order = ["寅","卯","辰","巳","午","未","申","酉","戌","亥","子","丑"]
    offset = month_order.index(month_dizhi)
    return TIANGAN[(start_idx + offset) % 10]

