class YaoType(str, Enum):
    LAO_YIN   = "老阴"   # 6  — changing yin
    SHAO_YANG = "少阳"   # 7  — static yang
    SHAO_YIN  = "少阴"   # 8  — static yin
    LAO_YANG  = "老阳"   # 9  — changing yang

class LineType(str, Enum):
    YIN  = "阴"
    YANG = "阳"

def yaotype_to_line(yt: YaoType) -> LineType:
    return LineType.YANG if yt in (YaoType.SHAO_YANG, YaoType.LAO_YANG) else LineType.YIN

def is_changing(yt: YaoType) -> bool:
    return yt in (YaoType.LAO_YIN, YaoType.LAO_YANG)
