# ─────────────────────────────────────────────────────────────
# 11. DiZhi → hour range (time branch mapping)
# ─────────────────────────────────────────────────────────────

HOUR_DIZHI: List[Tuple[str, int, int]] = [
    # (dizhi, start_hour_24, end_hour_24)  — end is exclusive
    ("子", 23, 1), ("丑",  1, 3), ("寅",  3, 5), ("卯",  5, 7),
    ("辰",  7, 9), ("巳",  9,11), ("午", 11,13), ("未", 13,15),
    ("申", 15,17), ("酉", 17,19), ("戌", 19,21), ("亥", 21,23),
]

def hour_to_dizhi(hour: int) -> str:
    """Convert 24h hour integer to DiZhi branch (子时 spans 23-1)."""
    if hour == 23 or hour == 0:
        return "子"
    for dz, start, end in HOUR_DIZHI:
        if start <= hour < end:
            return dz
    return "子"

